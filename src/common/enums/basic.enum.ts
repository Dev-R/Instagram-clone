/** Enum for basic color */
export enum BasicColor {
    /** Default color */
    DEFAULT = 'sky',

    /** Success color */
    SUCCESS = 'green',

    /** Info color */
    INFO = 'indigo',

    /** Warning color */
    WARNING = 'yellow',

    /** Danger color */
    DANGER = 'red',

    /** Outline with default color */
    OUTLINE  = 'outline', 
    
    /** Light color */
    LIGHT = 'light',

    /** Dark color */
    DARK = 'dark'
}